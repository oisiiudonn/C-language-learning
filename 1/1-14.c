/*入力中の文字の種類ごろにヒストグラムをかけ*/
#include <stdio.h>

int main(){

    int c;

    int ANCII[126];

    for(int i=0;i<127;i++){
        ANCII[i] = 0;
    }

    while((c = getchar()) != EOF){
        ANCII[c]++;
    }

    for(int i=33;i<127;i++){
        printf("%c:",i);
        for(int j=0;j<ANCII[i];j++){
            printf("[]");
        }
        printf("\n");
    }
}

